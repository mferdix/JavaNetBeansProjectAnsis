    protected void datatable() {
        Object[] Baris = {"ID", "Jenis Pelatihan", "Nama Pelatihan", "Tempat Pelatihan", "Metode Pelatihan", "Nama Peneliti"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelPelatihan.setModel(tabmode);
        String sql = "SELECT * FROM pelatihan";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()) {
                String a = hasil.getString("id");
                String b = hasil.getString("jenisPelatihan");
                String c = hasil.getString("namaPelatihan");
                String d = hasil.getString("tempatPelatihan");
                String e = hasil.getString("metodePelatihan");
                String f = hasil.getString("namaPeneliti");
                
                String[] data = {a,b,c,d,e,f};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            
        }
        
    }
    
