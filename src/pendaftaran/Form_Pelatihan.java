    private void bnextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnextActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO pelatihan VALUES (?,?,?,?,?,?)";
        
        try {
            
            PreparedStatement stat = conn.prepareStatement(sql);
            
            int max = 9999999;
            int min = 1000000;
            int range = max - min + 1;
            
            int rand = (int)(Math.random() * range) + min;
            
            stat.setString(1, "PemVis" + Integer.toString(rand) + "ID");
            stat.setString(2, jenisPelatihan.getSelectedItem().toString()); 
            stat.setString(3, namaPelatihan.getText());          
            stat.setString(4, tempatPelatihan.getText());
            stat.setString(5, metodePelatihan.getSelectedItem().toString()); 
            stat.setString(6, namaPeneliti.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            datatable();
            
            Form_Proses tugas = new Form_Proses();
            tugas.setVisible(true);
            this.setVisible(false);
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Data Gagal Disimpan " + e);
        }
    }//GEN-LAST:event_bnextActionPerformed

