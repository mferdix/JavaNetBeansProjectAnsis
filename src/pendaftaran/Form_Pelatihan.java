    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
            
        try {
            String sql = "UPDATE pelatihan SET jenisPelatihan = ?, namaPelatihan = ?, tempatPelatihan = ?, metodePelatihan = ?, namaPeneliti = ? where id = '" + tid.getText() + "'";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, jenisPelatihan.getSelectedItem().toString());
            stat.setString(2, namaPelatihan.getText());
            stat.setString(3, tempatPelatihan.getText());
            stat.setString(4, metodePelatihan.getSelectedItem().toString());
            stat.setString(5, namaPeneliti.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            tid.requestFocus();
            datatable();
            
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah, " + e);
        }
    }//GEN-LAST:event_beditActionPerformed

